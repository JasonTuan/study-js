// Khai báo biến
let a = 10;
let b = 20;

// Sử dụng toán tử số học
let sum = a + b;
let difference = a - b;
let product = a * b;
let quotient = a / b;
let remainder = a % b;

// In kết quả ra console
console.log("Sum:", sum); // Sum: 30
console.log("Difference:", difference); // Difference: -10
console.log("Product:", product); // Product: 200
console.log("Quotient:", quotient); // Quotient: 0.5
console.log("Remainder:", remainder); // Remainder: 10

// Sử dụng toán tử so sánh và logic
console.log("Is a equal to b?", a == b); // Is a equal to b? false
console.log("Is a not equal to b?", a != b); // Is a not equal to b? true
console.log("Is a greater than b?", a > b); // Is a greater than b? false
console.log("Is a less than or equal to b?", a <= b); // Is a less than or equal to b? true
console.log("Is a greater than 5 and b less than 25?", a > 5 && b < 25); // Is a greater than 5 and b less than 25? true
console.log("Is a less than 5 or b greater than 15?", a < 5 || b > 15); // Is a less than 5 or b greater than 15? true
